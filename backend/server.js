app.get('/api/nft/:collectionId', async (req, res) => {
  const { collectionId } = req.params;

  if (!collectionId) {
    return res.status(404).json({ error: 'collectionId est requis' });
  }

  try {
    // Instancier le contrat pour la collection donnée
    const collectionContract = new ethers.Contract(collectionId, collectionAbi, provider);

    // Récupérer la valeur de `nextTokenId` pour savoir combien de tokens ont été créés
    const nextTokenId = await collectionContract.nextTokenId();

    const nfts = [];
    
    // Parcourir tous les tokens mintés (de 0 à nextTokenId - 1)
    for (let tokenId = 0; tokenId < nextTokenId; tokenId++) {
      try {
        // Récupérer l'image associée à chaque token
        const imgURI = await collectionContract.getCardImage(tokenId);
        
        // Récupérer le propriétaire du token
        const owner = await collectionContract.ownerOf(tokenId);

        nfts.push({
          tokenId,
          owner,
          image: imgURI || '',
        });
      } catch (err) {
        console.warn(`Erreur pour le tokenId ${tokenId} dans la collection ${collectionId}:`, err);
      }
    }

    // S'il n'y a pas de NFTs dans cette collection
    if (nfts.length === 0) {
      return res.status(200).json({ message: 'Aucun NFT minté dans cette collection.' });
    }

    res.json(nfts); // Renvoie tous les NFTs trouvés dans la collection
  } catch (error) {
    console.error('Erreur lors de la récupération des NFTs pour la collection', collectionId, ':', error);
    res.status(500).json({ error: 'Erreur lors de la récupération des NFTs' });
  }
});

